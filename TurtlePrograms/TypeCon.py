YOB="1998"
Age=26
Height=6.1

print("Year of Birth:", YOB, type(YOB))
print("Age:", Age, type(Age))
print("Height", Height, type(Height))
print("--------------------------------------")

#Converting String into integer
Integer_from_string=int(YOB)
print("Year of Birth:", Integer_from_string, type(Integer_from_string))

#Converting Integer to Float
Float_from_integer=float(Age)
print("Age:", Float_from_integer, type(Float_from_integer))

#Converting Float to String
String_from_float=str(Height)
print("Height:", String_from_float, type(String_from_float))
